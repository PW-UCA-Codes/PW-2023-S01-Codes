//Logica
const _removeCard = (array, id) => array.filter(e => e.id !== id);
const removeCard = (id) => {
    data = _removeCard(data, id);
    renderCards(data);
}

//generaciÃ³n de cards
const createCard = ({ id, first_name, last_name, email, avatar, description }) => {
    const card = document.createElement("article");

    card.classList.add("card");
    card.dataset["id"] = id;

    card.innerHTML = `
        <figure><img src="${avatar}" alt="Avatar"></figure>
        <h3>${first_name} ${last_name}</h3>
        <h3 class="email"> ${email} </h3>
        <div class="text-container">
            ${textToParagraphs(description)}
        </div>
        <div class="btn-container"><button> Aceptar </button><button onclick="removeCard('${id}')"> Eliminar </button></div>
    `;

    return card;
}

const textToParagraphs = (text) => {
    const ps = text.split("\n\n");
    
    const elements = ps.map(t => {
        return `<p> ${t} </p>`
    });

    /* console.log(ps, elements); */
    return elements.join("\n");
}

const renderCards = (cardsInfo = []) => {
    /* cardsSection.appendChild(
        createCard(testElement)
    ); */

    cardsSection.innerHTML = "";

    cardsInfo.forEach(cardInfo => {
        const card = createCard(cardInfo);
        cardsSection.appendChild(card);
    });


}

